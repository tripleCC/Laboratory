static double perf_ccprime_miller_rabin(size_t loops, cc_size mr_depth)
{
    cc_size n = ccn_nof_size(sizeof(PRIME));
    cc_unit p[n];
    ccn_read_uint(n, p, sizeof(PRIME), PRIME);

    perf_start();

    do {
        if (ccprime_rabin_miller(n, p, mr_depth, rng) != 1) {
            abort();
        }
    } while (--loops != 0);

    return perf_seconds();
}

#define _TEST(_x)                      \
    {                                  \
        .name = #_x, .func = perf_##_x \
    }
static struct ccprime_perf_test {
    const char *name;
    double (*func)(size_t loops, cc_size nbits);
} ccprime_perf_tests[] = {
    _TEST(ccprime_miller_rabin),
};

static double perf_ccprime(size_t loops, size_t *psize, const void *arg)
{
    const struct ccprime_perf_test *test = arg;
    return test->func(loops, *psize);
}

static struct ccperf_family family;

struct ccperf_family *ccperf_family_ccprime(int argc, char *argv[])
{
    F_GET_ALL(family, ccprime);
    static const size_t group_nbits[] = { 1, 2, 16, 32 };
    F_SIZES_FROM_ARRAY(family, group_nbits);
    family.size_kind = ccperf_size_bits;
    return &family;
}
